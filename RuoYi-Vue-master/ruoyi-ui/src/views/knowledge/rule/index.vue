      :limit.sync="queryParams.pageSize"
      @pagination="getList"
    />

    <!-- 添加或修改发送规则对话框 -->
    <el-dialog :title="title" :visible.sync="open" width="500px" append-to-body>
      <el-form ref="form" :model="form" :rules="rules" label-width="80px">
        <el-form-item label="通用数量" prop="generalnum">
          <el-input v-model="form.generalnum" placeholder="请输入通用数量" />
        </el-form-item>
        <el-form-item label="专项数量" prop="securitynum">
          <el-input v-model="form.securitynum" placeholder="请输入专项数量" />
        </el-form-item>
        <el-form-item label="安全数量" prop="specialnum">
          <el-input v-model="form.specialnum" placeholder="请输入安全数量" />
        </el-form-item>
      </el-form>
      <div slot="footer" class="dialog-footer">
        <el-button type="primary" @click="submitForm">确 定</el-button>
        <el-button @click="cancel">取 消</el-button>
      </div>
    </el-dialog>
  </div>
